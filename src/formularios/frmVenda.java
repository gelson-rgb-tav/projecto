    private void Totais() {
        int Numero = tblvenda.getRowCount();

        int somaquntidade = 0, somavalor = 0;

        for (int i = 0; i < Numero; i++) {
            somaquntidade += Utilidades.objectToInt(tblvenda.getValueAt(i, 3));
            somavalor += Utilidades.objectToInt(tblvenda.getValueAt(i, 4));

        }
        txtTotalquantidade.setText("" + somaquntidade);
        txtvalor.setText("" + somavalor);
    }


