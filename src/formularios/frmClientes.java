    private void cmdsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdsalvarActionPerformed

        if (txtidcliente.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Digite o ID");
            txtidcliente.requestFocusInWindow();
            return;
        }
        if (cmbindetifica.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecione a tua identificação");
            cmbindetifica.requestFocusInWindow();
            return;
        }
        if (cmbbairo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecione o teu bairo");
            cmbbairo.requestFocusInWindow();
            return;
        }
        if (txtnome.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Digite o teu nome");
            txtnome.requestFocusInWindow();
            return;
        }
        if (txtsobnome.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Digite o teu sobre nome");
            txtsobnome.requestFocusInWindow();
            return;
        }
        if (txtendereco.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Digite o teu endereso");
            txtendereco.requestFocusInWindow();
            return;
        }
        if (txttelefone.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Deiche o seu número de TELEFONE");
            txttelefone.requestFocusInWindow();
            return;
        }
        if (txtemail.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Deiche o seu coreio Eletronico (Email)");
            txtemail.requestFocusInWindow();
            return;
        }
       
       

        int poslinha = clsdados.LinhaUsuario(txtidcliente.getText());
        if (cmdNovo) {
            if (poslinha != -1) {
                JOptionPane.showMessageDialog(rootPane, "Já existe este cadastro");
                txtidcliente.requestFocusInWindow();
                return;
            }
        } else if (poslinha == -1) {
            JOptionPane.showMessageDialog(rootPane, "Não existe este cadastro");
            txtidcliente.requestFocusInWindow();
            return;
        }

        
        Cliente MCliente = new Cliente(Integer.parseInt(txtidcliente.getText()), cmbindetifica.getSelectedIndex(), txtnome.getText(), 
                txtsobnome.getText(), txtendereco.getText(), 
                txttelefone.getText(), cmbbairo.getSelectedIndex(), canlcvenda.getDate(), canlcnasc.getDate(), txtemail.getText());
        String msg;
        if (cmdNovo) {
            msg = clsdados.CadCliente(MCliente);
        } else {
            msg = clsdados.EditarCliente(MCliente, poslinha);
        }

        JOptionPane.showMessageDialog(rootPane, msg);

        // guardar:
        cmdprimero.setEnabled(true);
        cmdanterior.setEnabled(true);
        cmdseguinte.setEnabled(true);
        cmdultimo.setEnabled(true);
        cmdnovo.setEnabled(true);
        cmdcancelar.setEnabled(false);
        cmdsalvar.setEnabled(false);
        cmdeditar.setEnabled(true);
        cmdpesqusar.setEnabled(true);

        txtidcliente.setEnabled(false);
        cmbindetifica.setEnabled(false);
        txtnome.setEnabled(false);
        txtsobnome.setEnabled(false);
        txtendereco.setEnabled(false);
        txtemail.setEnabled(false);
        txttelefone.setEnabled(false);
        txtemail.setEnabled(false);
        cmbbairo.setEnabled(false);
        canlcvenda.setEnabled(true);
        canlcnasc.setEnabled(false);

        CarregarTable();
    }//GEN-LAST:event_cmdsalvarActionPerformed

