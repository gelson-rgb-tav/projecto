    private void visulisarCadastro() {
        txtidcliente.setText("" +clsdados.getCliente()[clienteatual].getIdcliente());
        txtnome.setText("" + clsdados.getCliente()[clienteatual].getNome());
        txtsobnome.setText(clsdados.getCliente()[clienteatual].getSobnome());
        txttelefone.setText(clsdados.getCliente()[clienteatual].getTelefone());
        txtendereco.setText(clsdados.getCliente()[clienteatual].getEndereco());
        txtemail.setText(clsdados.getCliente()[clienteatual].getEmail());
        cmbindetifica.setSelectedItem(clsdados.getCliente()[clienteatual].getIdentificacao());
        cmbbairo.setSelectedItem(clsdados.getCliente()[clienteatual].getBairo());
        canlcvenda.setDate(clsdados.getCliente()[clienteatual].getDatavenda());
        canlcnasc.setDate(clsdados.getCliente()[clienteatual].getDatanascimento());
    }

    private void CarregarTable() {
        String titulocabesalho[] = {"ID", "NOME", "SOBRENOME", "IDENTF", "BAIRO", "DATA", "D-NASC"};
        String RegCadastro[] = new String[7];
        Usertabe = new DefaultTableModel(null, titulocabesalho);
        for (int i = 0; i < clsdados.NCliente(); i++) {
            RegCadastro[0] = "" + clsdados.getCliente()[i].getIdcliente();
            RegCadastro[1] = clsdados.getCliente()[i].getNome();
            RegCadastro[2] = clsdados.getCliente()[i].getSobnome();
            RegCadastro[3] = Id(clsdados.getCliente()[i].getIdentificacao());
            RegCadastro[4] = Bairo(clsdados.getCliente()[i].getBairo());
            RegCadastro[5] = "" + clsdados.getCliente()[i].getDatavenda();
            RegCadastro[6] = "" + clsdados.getCliente()[i].getDatanascimento();

            Usertabe.addRow(RegCadastro);

        }
        Ctable.setModel(Usertabe);
    }

    private String Id(int indc) {
        switch (indc) {
            case 0:
                return "BILHETE DE IDENTIDADE";

            case 1:
                return "PASSAPORTE";
            default:
                return "SEM IDENTIFICAÇÃO";
        }
    }

    private String Bairo(int bairo) {
        switch (bairo) {
            case 1:
                return "MISQUITA";
            case 2:
                return "ÁGUA BÔBÔ";
            case 3:
                return "PALHA";
            case 4:
                return "VILA MARIA";
            case 5:
                return "RUA 3 DE FEVEREIRO";
            case 6:
                return "ILHÉU S. AMARO";
            case 7:
                return "SANTANA";
            default:
                return "CIDADE CAPITAL";
        }
    }


